public class Card implements Comparable<Card>{
	
    public Card(int id1)
    { 
    	id = id1;
    }

    public int getId()
    {
    	return id;
    }
    public boolean equals (Object other)  
    {
    	return (id == ((Card)other).getId());
    }
    public int compareTo(Card other) 
    {
    	return  id - ((Card)other).getId(); 
    }
    public String toString()  
    {
    	return id + ""; 
    }

    //  Fields:
    private int id;
}
